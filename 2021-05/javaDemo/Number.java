public class Number {
    public static void main(String[] args) {
        System.out.println(5.0/0);
        System.out.println(5.0/-0);
        System.out.println(-5.0/0);
        System.out.println(0.0/0);

        char zero = '0';
        char A = 'A';
        char a = 'a';
        System.out.println((int) zero);
        System.out.println((int) A);
        System.out.println((int) a);
    }
}
